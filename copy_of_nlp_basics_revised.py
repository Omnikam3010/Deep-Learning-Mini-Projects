"""TF-IDF (Term Frequency — Inverse Document Frequency)
What to paste in your Text Box:

What is it?
An improvement over Bag of Words that weights words by importance, not just how many times they appear.

The Intuition:

TF (Term Frequency): How often a word appears in a specific message.

IDF (Inverse Document Frequency): Reduces the weight of words that appear in almost every message (like "the", "is", "to").

Why it's better for Spam:
A word like "WINNER" might appear many times in one spam email but rarely in your normal emails. TF-IDF gives "WINNER" a high score and "the" a low score.
"""


# Step 1: Define which features (words) the model should look for
def word_features(words, top_words):
    """Returns a dictionary: {word: True} if word is in the review, else False"""
    word_set = set(words)
    return {word: (word in word_set) for word in top_words}

# Step 2: Build the 'Vocabulary' (The top 2,000 most frequent words)
all_words = nltk.FreqDist(w.lower() for w in movie_reviews.words())
top_words = list(all_words.keys())[:2000]

# Step 3: Create labeled feature sets
feature_sets = [
    (word_features(movie_reviews.words(fileid), top_words), category)
    for category in movie_reviews.categories()
    for fileid in movie_reviews.fileids(category)
]

# Step 4: Shuffle and Split (Standard 75/25 split)
random.seed(42)
random.shuffle(feature_sets)
train_set, test_set = feature_sets[:1500], feature_sets[1500:]

# Step 5: Train the NLTK Naive Bayes Classifier
nb_clf = NaiveBayesClassifier.train(train_set)


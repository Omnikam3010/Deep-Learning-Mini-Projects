"""---
## 9. 🔢 Word Embeddings (Word2Vec)
🔢 Word Embeddings (Word2Vec)What are word embeddings?Word embeddings are dense vector representations where semantically similar words are placed close to each other in a multi-dimensional vector space. Unlike Bag of Words or TF-IDF, which create sparse vectors based on word counts, embeddings capture the "meaning" and relationships between words.Key Differences:Meaning Capture: Embeddings can solve analogies like $king - man + woman \approx queen$.Dimensions: Vectors are typically dense (100–300 dimensions) rather than the size of the entire vocabulary.Context: They rely on the distributional hypothesis: "A word is known by the company it keeps".Word2Vec Approaches:CBOW (Continuous Bag of Words): Predicts a target word based on its surrounding context.Skip-gram: Predicts the surrounding context words given a single target word.
"""

from gensim.models import Word2Vec
from nltk.tokenize import word_tokenize

# Step 1: Training Corpus
# In practice, Word2Vec needs millions of sentences to learn meaningful patterns.
sentences = [
    "The king is a powerful man who rules the kingdom",
    "The queen is a powerful woman who rules the kingdom",
    "The man likes to play football and watch movies",
    "The woman likes to read books and paint pictures",
    "Dogs are loyal animals and make wonderful pets",
    "Cats are independent animals and make wonderful pets",
    "Paris is the beautiful capital city of France",
    "Berlin is the historic capital city of Germany",
    "Machine learning and deep learning are subfields of AI",
    "Natural language processing is a subfield of AI"
]

# Step 2: Tokenization
# word_tokenize converts raw strings into lists of lowercase tokens.
tokenized = [word_tokenize(s.lower()) for s in sentences]

# Step 3: Train Word2Vec Model
model = Word2Vec(
    sentences=tokenized,
    vector_size=50,   # Dimensionality of the embedding space
    window=3,         # How many words to look at on each side
    min_count=1,      # Include all words (even those that appear once)
    workers=2,        # CPU threads used for training
    epochs=100        # Passes over the training data
)

print("✅ Word2Vec model trained!")
print(f"📚 Vocabulary size: {len(model.wv.key_to_index)} words")

# Step 4: Semantic Similarity
# Finding words with the highest cosine similarity to 'king'.
print('\n🔍 Words most similar to king:')
for word, sim in model.wv.most_similar('king', topn=4):
    print(f"   {word:<15} cosine similarity: {sim:.4f}")

# Step 5: Inspecting the Vector
# Each word is represented by 50 floats (the embedding).
print('\n📐 First 8 dimensions of vector for king:')
print(model.wv['king'][:8].round(4))

# --- Compare word pair similarities ---
# Cosine similarity range: 1.0 (identical) to -1.0 (opposite)
pairs = [
    ('king', 'queen'),
    ('dog', 'cat'),
    ('paris', 'berlin'),
    ('man', 'woman'),
    ('king', 'dog'),    # Expected to be much lower
]

print("📊 Word Pair Cosine Similarities:")
for w1, w2 in pairs:
    try:
        # wv.similarity calculates the cosine distance between two word vectors
        sim = model.wv.similarity(w1, w2)
        print(f"   {w1} ↔ {w2}: {sim:.4f}")
    except KeyError as e:
        # Word2Vec can only compare words that were in the training 'sentences'
        print(f"   ⚠️ Not in vocab: {e}")

